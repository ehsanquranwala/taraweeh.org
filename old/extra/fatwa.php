    <td scope="col"><br />At Jama Masjid of Block 5 Nazimabad, Karachi in 1989 some people brought a fatwa against Dora Tarjuma Quran from Jamia Islamia Binnori Town according to which this program was said to be against Shariat and a Bid'at. But this fatwa was not issued by the head of the department of Fatawa of the above Madarsa rather given by a junior faculty member.
        Moreover Anjuman Khuddam ul Quran contacted two top Islamic Institutes in Lahore namely Jamia Ashrafia and Jamia Naeemia and in Karachi an application was sent to the institute (Dar ul Uloom) founded by Mufti Muhammad Shafi for guidance in this regard.
